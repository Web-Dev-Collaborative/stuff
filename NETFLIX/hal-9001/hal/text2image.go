// Utilities to render text to an image. Useful for sending things like
// AsciiTable and Utf8Table to chat services that aren't consistent about
// fixed-width rendering. Might be fun for plugins too...
//
// A fixed-width font is embedded in this file to avoid depdending on external
// font files/paths.
//
// IBM VGA 8 font
// via http://int10h.org/oldschool-pc-fonts/
// Font License: Creative Commons Attribution-ShareAlike 4.0 International
//
// converted with a hacked-up version of dewinfont from
// http://www.chiark.greenend.org.uk/~sgtatham/fonts/dewinfont
//
// This could be smaller, etc. with tighter encoding but I wanted
// to keep the binascii encoding for easy tweaking.

import (
	"image"
	"image/color"
	"image/draw"
	"strconv"
	"unicode/utf8"
)

// FontChar is a single character of the font.
// The Code field might go away. It was extracted from the original font
// and was flaky so I switched to using the UTF8-string to index and locate
// glyphs.
type FontChar struct {
	String string // the UTF-8 single character
	Code   uint
	Width  uint
	Value  [16]uint8
}

// FontData is a font and its metadata.
type FontData struct {
	Facename  string
	Height    int
	Width     int
	Ascent    int
	Pointsize int
	Weight    int
	Charset   int
	r2char    map[rune]FontChar // index for quick lookup
	Chars     [256]FontChar
}

// charRow converts a string representation of binary uint8 to uint8.
// This is a bit inefficient but it keeps the font easy to edit and
// it only needs to run once.
func charRow(in string) (out uint8) {
	for i, r := range in {
		if r == '1' {
			out |= (1 << uint8(i))
		}
	}

	return out
}

// StringToChars takes a string and returns the FontChar
func (fd *FontData) StringToChars(want string) []*FontChar {
	runes := utf8.RuneCountInString(want)
	out := make([]*FontChar, runes)
	errr := fd.r2char['█']

	i := 0
	for _, r := range want {
		if c, exists := fd.r2char[r]; exists {
			out[i] = &c
		} else {
			// if the character is unknown, return a full block
			out[i] = &errr
		}
		i++
	}

	return out
}

// StringToImages takes a string and returns a list of images
func (fd *FontData) StringToImages(want string, clr color.Color) []image.Image {
	chars := fd.StringToChars(want)
	out := make([]image.Image, len(chars))

	rect := image.Rect(0, 0, fd.Width, fd.Height)

	// TODO: maybe add a field to FontChar to cache or pregenerate the images
	for i, c := range chars {
		img := image.NewRGBA(rect)
		var x uint
		for y, row := range c.Value {
			for x = 0; x < c.Width; x++ {
				if row&uint8(1<<x) != 0 {
					img.Set(int(x), y, clr)
				}
			}
		}
		out[i] = img
	}

	return out
}

// StringToImage takes a string and renders it to an image.Image.
// Newlines are not respected. See: StringsToImage().
func (fd *FontData) StringToImage(want string, clr color.Color) image.Image {
	runes := utf8.RuneCountInString(want)
	outrect := image.Rect(0, 0, fd.Width*runes, fd.Height)
	out := image.NewRGBA(outrect)

	chars := fd.StringToImages(want, clr)

	for i, char := range chars {
		sr := char.Bounds()
		dp := image.Pt(i*fd.Width, 0)
		r := image.Rectangle{dp, dp.Add(sr.Size())}
		draw.Draw(out, r, char, sr.Min, draw.Src)
	}

	return out
}

// StringsToImage renders an array of strings to an image, with each row in
// the provided list as a line. The image size is dependent on the text
// passed in.
func (fd *FontData) StringsToImage(want []string, clr color.Color) image.Image {
	var max int
	for _, str := range want {
		runes := utf8.RuneCountInString(str)
		if runes > max {
			max = runes
		}
	}

	width := max * fd.Width
	height := len(want) * fd.Height
	size := image.Rect(0, 0, width, height)
	out := image.NewRGBA(size)

	for y, str := range want {
		row := fd.StringToImage(str, clr)

		sr := row.Bounds()
		dp := image.Pt(0, y*fd.Height)
		r := image.Rectangle{dp, dp.Add(sr.Size())}

		draw.Draw(out, r, row, sr.Min, draw.Src)
	}

	return out
}

